# Начало программы должно начинаться только с program var #
program var

# Объявление переменных идет после program var #
% x, y, z, a, b

begin
    let x = 5
    y = x + 3
    z = 1
    x = y >= z
    x = 5 or 2

    input(a)
    input(a b)
    input(a b z)

    output(a + b z)
    output(a + b)
    output(a or b)
    output(a)

    do while
        x = y
        let z = x + y
    loop

end.
